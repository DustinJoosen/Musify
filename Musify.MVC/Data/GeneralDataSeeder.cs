        public GeneralDataSeeder SeedUsers()
        {
            var fakedUsers = new Faker<User>()
                .RuleFor(c => c.Id, f => ++f.IndexVariable)
                .RuleFor(c => c.Username, f => f.Internet.UserName())
                .RuleFor(c => c.Name, f => f.Person.FullName)
                .RuleFor(c => c.Password, f => BCrypt.Net.BCrypt.HashPassword("password"))
                .RuleFor(c => c.BirthDate, f => f.Person.DateOfBirth)
                .Generate(100);

            _modelBuilder.Entity<User>()
                .HasData(fakedUsers);

            return this;
        }

